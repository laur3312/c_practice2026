#include <stdio.h>

int main(void) {
    // --- ШАГ 1: ЗАПИСЬ В ФАЙЛ ---
    FILE *fp = fopen("letter.txt", "w"); // Открываем файл для записи
    if (fp == NULL) return 1;            // Проверяем, открылся ли файл

    fputc('A', fp);                     // Записываем один символ 'A'
    fclose(fp);                         // Обязательно закрываем файл


    // --- ШАГ 2: ЧТЕНИЕ ИЗ ФАЙЛА ---
    fp = fopen("letter.txt", "r");       // Открываем тот же файл для чтения
    if (fp == NULL) return 1;            // Снова проверяем на ошибку

    int ch = fgetc(fp);                  // Читаем один символ из файла
    printf("Мы прочитали букву: %c\n", ch); // Выводим её на экран

    fclose(fp);                         // Закрываем файл в конце
    return 0;
}